public class bujursangkar {
    int sisi = 6;
    int hasil;

    public void  keliling(){
        hasil = 4*sisi;
        System.out.println("Keliling dari bujursangkar di atas adalah " +hasil +" Cm");
    }
    public static void main(String[] args){
        bujursangkar kll = new bujursangkar();
        kll.keliling();
    }

}
